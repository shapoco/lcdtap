constexpr uint16_t SPLASH_WIDTH = 128;
constexpr uint16_t SPLASH_HEIGHT = 64;

// Fills an RGB565 framebuffer with the splash image, centered (1=white,
// 0=black). Any margin outside the 128x64 image area is cleared to black,
// so this can be used directly on framebuffers of other sizes too.
//
// fillScanline() applies outputRotation as a true geometric rotation of the
// framebuffer content, so an image drawn upright here would come out
// rotated on screen. rot=1/3 (90/270) already transpose the buffer axes,
// which drawSplash() cannot pre-correct for on a fixed-orientation image;
// only the rot=2/3 (180/270) point-mirror component is compensated by
// pre-flipping the source pixels 180 degrees.
inline void drawSplash(uint16_t *fb, uint16_t buffWidth, uint16_t buffHeight,
                       uint8_t outputRotation) {
  constexpr uint16_t STRIDE = SPLASH_WIDTH / 8;
  const bool flip180 = (outputRotation & 3u) == 1 || (outputRotation & 3u) == 2;
  const int16_t offX = ((int16_t)buffWidth - (int16_t)SPLASH_WIDTH) / 2;
  const int16_t offY = ((int16_t)buffHeight - (int16_t)SPLASH_HEIGHT) / 2;
  for (uint16_t y = 0; y < buffHeight; ++y) {
    uint16_t *row = fb + (uint32_t)y * buffWidth;
    int16_t sy = (int16_t)y - offY;
    for (uint16_t x = 0; x < buffWidth; ++x) {
      int16_t sx = (int16_t)x - offX;
      bool on = false;
      if (sx >= 0 && sx < (int16_t)SPLASH_WIDTH && sy >= 0 &&
          sy < (int16_t)SPLASH_HEIGHT) {
        uint16_t ix = flip180 ? (SPLASH_WIDTH - 1 - sx) : sx;
        uint16_t iy = flip180 ? (SPLASH_HEIGHT - 1 - sy) : sy;
        uint8_t b = splashImage[(uint32_t)iy * STRIDE + (ix >> 3)];
        on = (b >> (7 - (ix & 7))) & 1;
      }
      row[x] = on ? 0xFFFFu : 0x0000u;
    }
  }
}

